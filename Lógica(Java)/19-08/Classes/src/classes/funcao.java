package classes;

public class funcao{
    public float soma(float a,float b){
        float d = a + b;
        return d;
    }
    public float subtrair(float a,float b){
        float sub = a - b;
        return sub;
    }
    public float multiplicar(float a,float b){
        float mult = a * b;
        return mult;
    }
    public float dividir(float a, float b){
        float div = a/b;
        return div;  
    }
}
